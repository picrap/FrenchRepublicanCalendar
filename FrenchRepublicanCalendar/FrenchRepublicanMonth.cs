namespace FrenchRepublicanCalendar
{
    /// <summary>
    ///     Months enumeration.
    ///     Yes, there are 13 months :)
    /// </summary>
    public enum FrenchRepublicanMonth
    {
        /// <summary>
        ///     Vendémiaire (month of "vendanges", grape harvest, yeah, French spirit!), first month (sep-oct)
        /// </summary>
        Vendémiaire = 1,

        /// <summary>
        ///     Brumaire (month of "brumes", fog), second month (oct-nov)
        /// </summary>
        Brumaire,

        /// <summary>
        ///     Frimaire (month of "frimas", cold), third month (nov-dec)
        /// </summary>
        Frimaire,

        /// <summary>
        ///     Nivôse (month of snow), fourth month (dec-jan)
        /// </summary>
        Nivôse,

        /// <summary>
        ///     Pluviôse (month of "pluie", rain), fifth month (jan-feb)
        /// </summary>
        Pluviôse,

        /// <summary>
        ///     Ventôse (month of "vent", wind), sixth month (feb-mar)
        /// </summary>
        Ventôse,

        /// <summary>
        ///     Germinal (month of germination), seventh month (mar-apr)
        /// </summary>
        Germinal,

        /// <summary>
        ///     Floréal (month of flowers), eighth month (apr-may)
        /// </summary>
        Floréal,

        /// <summary>
        ///     Prairial (month of "prairies", meadows), ninth month (may-jun)
        /// </summary>
        Prairial,

        /// <summary>
        ///     Messidor (month of "moisson", harvest), tenth month (jun-jul)
        /// </summary>
        Messidor,

        /// <summary>
        ///     Thermidor (month of hot temperature), eleventh month (jul-aug)
        /// </summary>
        Thermidor,

        /// <summary>
        ///     Fructidor (month of fruits), twelfth month (aug-sep)
        /// </summary>
        Fructidor,

        /// <summary>
        ///     Sans-culottides, extra month (non-labor days) (sep)
        /// </summary>
        SansColuttides
    }
}