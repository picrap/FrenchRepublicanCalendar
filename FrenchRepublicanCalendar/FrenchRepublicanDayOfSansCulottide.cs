namespace FrenchRepublicanCalendar
{
    /// <summary>
    ///     Enumeration for last days of year, which don't have the same name
    /// </summary>
    public enum FrenchRepublicanDayOfSansCulottide
    {
        /// <summary>
        ///     Vertu (virtue), first day
        /// </summary>
        Vertu,

        /// <summary>
        ///     Génie (genius), second day
        /// </summary>
        Génie,

        /// <summary>
        ///     Travail (labor), third day
        /// </summary>
        Travail,

        /// <summary>
        ///     Opinion, fourth day
        /// </summary>
        Opinion,

        /// <summary>
        ///     Récompenses (rewards), fifth day
        /// </summary>
        Récompenses,

        /// <summary>
        ///     Révolution (revolution!), sixth day
        ///     Occurs once in approximately four years
        /// </summary>
        Révolution
    }
}